static const Font fonts[FONT_COUNT] = {
	{tex_font,      8,  fnt_gameboy_glyphs},
	{tex_ms_gothic, 12, fnt_ms_gothic_glyphs},
};

static Mix_Chunk* sounds[SOUND_COUNT];

void LoadAssets() {
	{
		IMG_Init(IMG_INIT_PNG);

		textures[tex_font] = IMG_LoadTexture(game->renderer, "textures/font.png");
		textures[tex_nineslice] = IMG_LoadTexture(game->renderer, "textures/nineslice.png");
		textures[tex_pokemon_back] = IMG_LoadTexture(game->renderer, "textures/pokemon_back.png");
		textures[tex_pokemon_front] = IMG_LoadTexture(game->renderer, "textures/pokemon_front.png");
		textures[tex_ms_gothic] = IMG_LoadTexture(game->renderer, "textures/ms_gothic.png");

		IMG_Quit();
	}

	{
		sounds[snd_slash] = Mix_LoadWAV("sounds/Slash.wav");
		sounds[snd_confirm] = Mix_LoadWAV("sounds/SFX_PRESS_AB.wav");
		sounds[snd_select] = Mix_LoadWAV("sounds/bfxr_select.wav");
		sounds[snd_hurt] = Mix_LoadWAV("sounds/SFX_COLLISION.wav");
	}
}

void UnloadAssets() {
	for (int i = SOUND_COUNT; i--;) {
		Mix_FreeChunk(sounds[i]);
	}

	for (int i = TEXTURE_COUNT; i--;) {
		SDL_DestroyTexture(textures[i]);
	}
}

SDL_Texture* GetTexture(u32 texture_index) {
	if (texture_index < TEXTURE_COUNT) {
		return textures[texture_index];
	} else {
		return textures[0];
	}
}

const Font* GetFont(u32 font_index) {
	if (font_index < FONT_COUNT) {
		return &fonts[font_index];
	} else {
		return &fonts[0];
	}
}

Mix_Chunk* GetSound(u32 sound_index) {
	if (sound_index < SOUND_COUNT) {
		return sounds[sound_index];
	} else {
		return sounds[0];
	}
}
